    private void LoadButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_LoadButtonMouseClicked
        try {
            FileInputStream fin = new FileInputStream("Vollo_illo.vec");
            ObjectInputStream ois = new ObjectInputStream(fin);
            ArrayList<Object> forRead = (ArrayList<Object>) ois.readObject();
            this.vehicle.setPlate((String) forRead.remove(0));
            this.vehicle.setPower((Double) forRead.remove(0));
            this.vehicle.setSeats((Integer) forRead.remove(0));
            regain();
        } catch (IOException ex) {
            Logger.getLogger(FigI.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(VehI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_LoadButtonMouseClicked

    private void SaveButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SaveButtonMouseClicked
        try {
            File f = new File("Vollo_illo.vec");
            if(f.exists()) f.delete();
            FileOutputStream fout = new FileOutputStream("Vollo_illo.vec");
            ObjectOutputStream oos = new ObjectOutputStream(fout);
            refresh();
            ArrayList<Object> toWrite = new ArrayList<>();
            toWrite.add(this.vehicle.getPlate());
            toWrite.add(this.vehicle.getPower());
            toWrite.add(this.vehicle.getSeats());
            oos.writeObject(toWrite);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(FigI.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(FigI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_SaveButtonMouseClicked

